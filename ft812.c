void spi_flash_write(const uint8_t *data, uint16_t len)
{
	uint16_t count;

	len = (len + 3)&(~3);

	for(count=0;count<len;count++)
	{
		spiTransfer(fetch_flash_byte(data+count));
	}

	FT8_inc_cmdoffset(len);
}
void block_transfer(const uint8_t *data, uint16_t len)
{
	uint16_t bytes_left;
	uint16_t block_len;
	uint32_t ftAddress;	

	bytes_left = len;
	while(bytes_left > 0)
	{
		block_len = bytes_left>4000 ? 4000:bytes_left;

		ftAddress = RAM_CMD + cmdOffset;
		ft_spi_cs_set();
		spiTransfer((uint8_t)(ftAddress >> 16) | WRITE_MEM); /* send Memory Write plus high address byte */
		spiTransfer((uint8_t)(ftAddress >> 8));	/* send middle address byte */
		spiTransfer((uint8_t)(ftAddress));		/* send low address byte */
		spi_flash_write(data,block_len);
		ft_spi_cs_reset();
		data += block_len;
		bytes_left -= block_len;
		FtCmdExecute();
	}	
}
