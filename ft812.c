static inline uint8_t fetch_flash_byte(const uint8_t *data){
    return *data;
}
void FT8_memWrite_flash_buffer(uint32_t ftAddress, const uint8_t *data, uint16_t len)
{
	uint16_t count;

	ft_spi_cs_set();
	spiTransfer((uint8_t)(ftAddress >> 16) | WRITE_MEM);
	spiTransfer((uint8_t)(ftAddress >> 8));
	spiTransfer((uint8_t)(ftAddress));

	len = (len + 3)&(~3);

	for(count=0;count<len;count++)
	{
		spiTransfer(fetch_flash_byte(data+count));
	}

	ft_spi_cs_reset();
}

