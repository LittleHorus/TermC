/* order the command co-processor to start processing its FIFO queue and do not wait for completion */
void FtCmdStart(void)
{
	uint32_t ftAddress;

	ftAddress = REG_CMD_WRITE;

	ft_spi_cs_set();
	spiTransfer((uint8_t)(ftAddress >> 16) | WRITE_MEM); /* send Memory Write plus high address byte */
	spiTransfer((uint8_t)(ftAddress >> 8));	/* send middle address byte */
	spiTransfer((uint8_t)(ftAddress));			/* send low address byte */
	spiTransfer((uint8_t)(cmdOffset));			/* send data low byte */
	spiTransfer((uint8_t)(cmdOffset >> 8));	/* send data high byte */
	ft_spi_cs_reset();
}
void FtCmdExecute(void)
{
	FtCmdStart();
	while (FtBusy());
}
